$("").click(function () {
    //바뀔놈을 잡아서 클릭하면 몬가 할거다
    if ($("").hasClass("")) {
        //해당 클래스가 있는지 확인해라
        //반환값 = Boolean
        $("").removeClass("");
        $("").find("").removeClass("");
        //부모 개체 안에서 <.find("") 자식요소를 잡아서> 지워라
    }
    $("").toggleClass("");
    $("").toggleClass("");
});

$("").on("click", function () {
    if ($("").hasClass("")) {
        $("").find("").removeClass("");
        $("").removeClass("");
    }
    $(this).toggleClass("");
    $("").toggleClass("");
    $("").toggleClass("");
});
